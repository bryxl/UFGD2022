const abi = document.querySelector("#abi-a");

abi.addEventListener("mouseover", event => {
    document.getElementById("firstScreen").className = "hide";
    document.getElementById("abi-b").className = "show";
});

abi.addEventListener("mouseout", event => {
    document.getElementById("firstScreen").className = "show";
    document.getElementById("abi-b").className = "hide";
});

